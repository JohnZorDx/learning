//Operadores Aritmeticos

let primeiroNumero = 20
let segundoNumero = 2

// adição: +
// subtração: -
// Multiplicação: *
// Divisão: /
// Exponenciação: **
// Resto da Divisão: %

const soma = primeiroNumero + segundoNumero
const subtracao = primeiroNumero - segundoNumero
const multiplicacao = primeiroNumero * segundoNumero
const Divisao = primeiroNumero / segundoNumero
const exponenciacao = primeiroNumero ** segundoNumero
const restoDaDivisao = primeiroNumero % segundoNumero


console.log('Soma =', soma)
console.log('Subtraçao =', subtracao)
console.log('Multiplicaçao =', multiplicacao)
console.log('Divisao =', Divisao)
console.log('Exponenciaçao =', exponenciacao)
console.log('Resto da Divisao =', restoDaDivisao)

